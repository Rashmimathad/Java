class River{

	static String riverName="Ganga";
	static String riverOrigin="Gangotri Glacier";
	static int flowLength=2525;
	static double phRange=8.3;
	static boolean hasMedicinalProperties=true;
	
	public static String getRiverName(){
		return riverName;
	}
	
	public static String getRiverOrigin(){
		return riverOrigin;
	}
	
	public static int getFlowLength(){
		return flowLength;
	}
	
	public static double getPhRange(){
		return phRange;
	}
	
	public static boolean getHasMedicinalProperties(){
		return hasMedicinalProperties;
	}
}